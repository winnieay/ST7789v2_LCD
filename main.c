void LCD_WriteCommand(uint8_t data){
	LCD_CS_LOW();
	LCD_DC_LOW();
	LCD_WriteSPI(data);
	LCD_DC_HIGH();
	LCD_CS_HIGH();
}

void LCD_WriteData(uint8_t data){
	LCD_CS_LOW();
	LCD_DC_HIGH();
	LCD_WriteSPI(data);
	LCD_DC_LOW();
	LCD_CS_HIGH();
}


void LCD_Address_Set(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2){
	
	LCD_WriteCommand(0x2b);	//row 

	LCD_WriteData(x1>>8);
	LCD_WriteData(x1);
	LCD_WriteData(x2>>8);
	LCD_WriteData(x2);
	

	LCD_WriteCommand(0x2a);	//col
	LCD_WriteData(y1>>8);
	LCD_WriteData(y1);
	LCD_WriteData(y2>>8);
	LCD_WriteData(y2);
	
	LCD_WriteCommand(0x2c);	//Memory write
}
void LCD_Fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
{          
	uint16_t i,j; 
	LCD_Address_Set(xsta,ysta,xend-1,yend-1);
	for(i=xsta;i<xend;i++)
	{													   	 	
		for(j=ysta;j<yend;j++)
		{
			LCD_WriteData(color);
			LCD_WriteData(color>>8);
		}
	} 					  	    
}

void LCD_DrawPoint(uint16_t x,uint16_t y,uint16_t color)
{
	LCD_Address_Set(x,y,x,y);
	LCD_WriteData(color);
	LCD_WriteData(color>>8);
} 

