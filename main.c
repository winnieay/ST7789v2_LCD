void LCD_ShowChar(uint16_t x,uint16_t y,uint8_t num,uint16_t fc)
{
	uint8_t temp,sizex,t,sizey=32;
	uint16_t i,TypefaceNum;
	uint16_t x0=x;
	sizex=sizey/2;
	TypefaceNum=(sizex/8+((sizex%8)?1:0))*sizey;
	num=num-' ';    
	LCD_Address_Set(x,y,x+sizex-1,y+sizey-1); 

for(i=0;i<TypefaceNum;i++)
	{ 
		for(t=0;t<8;t++)
		{
				temp=ascii_3216[num][i];
				if(temp&(0x01<<t))
					LCD_DrawPoint(x,y,fc);
				x++;
				if((x-x0)==sizex)
				{
					x=x0;
					y++;
					break;
				}
			}
		}
}

void LCD_ShowString(uint16_t x,uint16_t y,const char *p,uint16_t fc)
{      
	
	while(*p!='\0')
	{       
		LCD_ShowChar(x,y,*p,fc);
		x+=32/2;
		p++;
	}  
}

